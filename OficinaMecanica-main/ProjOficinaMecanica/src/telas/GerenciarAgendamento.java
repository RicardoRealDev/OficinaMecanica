    private int lerInteiro(String mensagem) {
        while (true) {
            try {
                String texto = JOptionPane.showInputDialog(mensagem);

                if (texto == null) {
                    return -1;
                }

                if (texto.trim().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Campo obrigatório!");
                } else {
                    return Integer.parseInt(texto);
                }

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Digite apenas números!");
            }
        }
    }
    
    public boolean conflitoAgendamento(String data,String hora,Veiculo veiculo,Mecanico mecanico) {

        for (Agendamento a : listaAgendamentos) {

            boolean mesmaData = a.getData().equals(data);
            boolean mesmaHora = a.getHora().equals(hora);

            if (mesmaData && mesmaHora) {

                if (a.getVeiculo().getPlaca()
                        .equalsIgnoreCase(veiculo.getPlaca())) {

                    JOptionPane.showMessageDialog(
                        null,
                        "Esse veículo já possui agendamento nesse horário!"
                    );

                    return true;
                }

                if (a.getMecanico().getCodigo()
                        == mecanico.getCodigo()) {

                    JOptionPane.showMessageDialog(
                        null,
                        "Esse mecânico já possui agendamento nesse horário!"
                    );

                    return true;
                }
            }
        }

        return false;
    }
    
    public boolean conflitoAgendamentoEdicao(String data, String hora,
        Veiculo veiculo, Mecanico mecanico, Agendamento agendamentoAtual) {

        for (Agendamento a : listaAgendamentos) {

            if (a.getCodigo() == agendamentoAtual.getCodigo()) {
                continue;
            }

            if (a.getData().equals(data) && a.getHora().equals(hora)) {

                if (a.getVeiculo().getPlaca().equalsIgnoreCase(veiculo.getPlaca())) {
                    JOptionPane.showMessageDialog(null, "Esse veículo já tem agendamento nesse horário!");
                    return true;
                }

                if (a.getMecanico().getCodigo() == mecanico.getCodigo()) {
                    JOptionPane.showMessageDialog(null, "Esse mecânico já tem agendamento nesse horário!");
                    return true;
                }
            }
        }

        return false;
    }
    
