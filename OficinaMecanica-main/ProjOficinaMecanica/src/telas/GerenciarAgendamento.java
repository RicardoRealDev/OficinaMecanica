    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (listaVeiculos.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Cadastre um veículo antes de criar um agendamento.");
            return;
        }
        
        if (listaProfissional.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Cadastre um profissional antes de criar um agendamento.");
            return;
        }
        
        
        int codigoProfissional = lerInteiro("Digite o código do profissional:");
        
        if (codigoProfissional == -1) {
            return;
        }
        
        
        String placa = JOptionPane.showInputDialog("Digite a placa do veículo:");        
        
        if (placa == null || placa.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Placa é obrigatória!");
            return;
        }
     
        Veiculo veiculo = principal.buscarVeiculoPlaca(placa);
        Profissional profissional = principal.buscarProfissionalCodigo(codigoProfissional);
        
        if (veiculo == null) {
            JOptionPane.showMessageDialog(null, "Veículo não encontrado!");
            return;
        }
        
        if (profissional == null) {
            JOptionPane.showMessageDialog(null, "Profissional não encontrado!");
            return;
        }
        
        if (!(profissional instanceof Mecanico)) {

            JOptionPane.showMessageDialog(null,"O profissional encontrado não é um mecânico!");
            return;
        }

        Mecanico mecanico = (Mecanico) profissional;
        

        
        int codigoAgendamento = lerInteiro("Digite o código do agendamento:");
        
        if (codigoAgendamento == -1) {
            return;
        }
        
        if (principal.buscarAgendamentoCodigo(codigoAgendamento) != null) {
            JOptionPane.showMessageDialog(null, "Já existe agendamento com esse código!");
            return;
        }
        
        String data = JOptionPane.showInputDialog("Digite a data (dd/MM/yyyy):");

        String hora = JOptionPane.showInputDialog("Digite a hora (HH:mm):");

        String tipoServico = JOptionPane.showInputDialog("Digite o tipo de serviço:");
        
        double valor;
        
        try {
            valor = Double.parseDouble(JOptionPane.showInputDialog("Digite o valor:"));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Digite um valor válido!");
            return;
        }
        
        if (data == null || data.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data é obrigatória!");
            return;
        }
        
        if (hora == null || hora.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Hora é obrigatória!");
            return;
        }
                
        if (tipoServico == null || tipoServico.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Tipo de serviço é obrigatório!");
            return;
        }
        

        
        if (!data.matches("\\d{2}/\\d{2}/\\d{4}")) {

            JOptionPane.showMessageDialog(
                null,
                "Data inválida! Use dd/MM/yyyy"
            );

            return;
        }
        
        if (!hora.matches("\\d{2}:\\d{2}")) {

            JOptionPane.showMessageDialog(
                null,
                "Hora inválida! Use HH:mm"
            );

            return;
        }
        
        
        if (conflitoAgendamento(data, hora, veiculo, mecanico)) {
            return;
        }
        
        Agendamento agendamento = new Agendamento(codigoAgendamento,data,hora,tipoServico,valor,veiculo,mecanico);
        listaAgendamentos.add(agendamento);
        
        JOptionPane.showMessageDialog(null,"Agendamento cadastrado com sucesso!");
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int codigo = lerInteiro("Digite o código do agendamento que deseja editar:");

        if (codigo == -1) {
            return;
        }

        Agendamento agendamento = principal.buscarAgendamentoCodigo(codigo);

        if (agendamento == null) {
            JOptionPane.showMessageDialog(null, "Agendamento não encontrado!");
            return;
        }

        String novaData = JOptionPane.showInputDialog("Digite a nova data (dd/MM/yyyy):");

        if (novaData == null || novaData.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data é obrigatória!");
            return;
        }

        if (!novaData.matches("\\d{2}/\\d{2}/\\d{4}")) {
            JOptionPane.showMessageDialog(null, "Data inválida! Use dd/MM/yyyy");
            return;
        }

        String novaHora = JOptionPane.showInputDialog("Digite a nova hora (HH:mm):");

        if (novaHora == null || novaHora.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Hora é obrigatória!");
            return;
        }

        if (!novaHora.matches("\\d{2}:\\d{2}")) {
            JOptionPane.showMessageDialog(null, "Hora inválida! Use HH:mm");
            return;
        }

        String novoTipoServico = JOptionPane.showInputDialog("Digite o novo tipo de serviço:");

        if (novoTipoServico == null || novoTipoServico.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Tipo de serviço é obrigatório!");
            return;
        }

        double novoValor;

        try {
            novoValor = Double.parseDouble(
                JOptionPane.showInputDialog("Digite o novo valor:")
            );
            if (novoValor < 0) {JOptionPane.showMessageDialog(null,"Valor inválido!");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Digite um valor válido!");
            return;
        }

        if (conflitoAgendamentoEdicao(novaData, novaHora, agendamento.getVeiculo(), agendamento.getMecanico(), agendamento)) {
            return;
        }

        agendamento.setData(novaData);
        agendamento.setHora(novaHora);
        agendamento.setTipoServico(novoTipoServico);
        agendamento.setValor(novoValor);

        JOptionPane.showMessageDialog(null, "Agendamento editado com sucesso!");
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int codigo = lerInteiro("Digite o código do agendamento que deseja excluir:");

        if (codigo == -1) {
            return;
        }

        Agendamento agendamento = principal.buscarAgendamentoCodigo(codigo);

        if (agendamento == null) {
            JOptionPane.showMessageDialog(null, "Agendamento não encontrado!");
            return;
        }

        int resposta = JOptionPane.showConfirmDialog(null,"Deseja realmente excluir este agendamento?\n\n"+ agendamento,"Confirmação",JOptionPane.YES_NO_OPTION);

        if (resposta == JOptionPane.YES_OPTION) {

            listaAgendamentos.remove(agendamento);

            JOptionPane.showMessageDialog(
                    null,
                    "Agendamento excluído com sucesso!"
            );
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (listaAgendamentos.isEmpty()) {
            JOptionPane.showMessageDialog(null,
                "Não há agendamentos cadastrados.");
            return;
        }
        
        
        taSaida.setText("");

        if (rbMecanico.isSelected()) {

            int codigo = lerInteiro("Digite o código do mecânico:");

            for (Agendamento a : listaAgendamentos) {

                if (a.getMecanico().getCodigo() == codigo) {

                    taSaida.append(a.toString() + "\n\n");
                }
            }

        } else if (rbVeiculo.isSelected()) {

            String placa = JOptionPane.showInputDialog("Digite a placa:");

            for (Agendamento a : listaAgendamentos) {

                if (a.getVeiculo().getPlaca().equalsIgnoreCase(placa)) {

                    taSaida.append(a.toString() + "\n\n");
                }
            }

        } else if (rbData.isSelected()) {

            String data = JOptionPane.showInputDialog("Digite a data (dd/MM/yyyy):");

            for (Agendamento a : listaAgendamentos) {

                if (a.getData().equals(data)) {

                    taSaida.append(a.toString() + "\n\n");
                }
            }

        }    
        
        if (taSaida.getText().isEmpty()) {
            taSaida.setText("Nenhum agendamento encontrado.");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

