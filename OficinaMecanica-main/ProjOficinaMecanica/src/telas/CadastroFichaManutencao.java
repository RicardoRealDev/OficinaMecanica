    private void btnInserirFichaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirFichaActionPerformed
        // TODO add your handling code here:
        if (listaVeiculos.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Cadastre um veículo antes de criar uma ficha.");
            return;
        }

        int codigo = lerInteiro("Digite o código da ficha:");

        if (codigo == -1) {
            return;
        }

        FichaManutencao fichaExistente = principal.buscarFichaCodigo(codigo);

        if (fichaExistente != null) {
            JOptionPane.showMessageDialog(null, "Já existe uma ficha com esse código.");
            return;
        }

        String placa = JOptionPane.showInputDialog("Digite a placa do veículo:");

        if (placa == null || placa.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Placa obrigatória.");
            return;
        }

        Veiculo veiculo = principal.buscarVeiculoPlaca(placa);

        if (veiculo == null) {
            JOptionPane.showMessageDialog(null, "Veículo não encontrado.");
            return;
        }

        FichaManutencao fichaPorPlaca = principal.buscarFichaPorPlaca(placa);

        if (fichaPorPlaca != null) {
            JOptionPane.showMessageDialog(null, "Esse veículo já possui ficha de manutenção.");
            return;
        }

        listaFichas.add(new FichaManutencao(codigo, veiculo));

        JOptionPane.showMessageDialog(null, "Ficha cadastrada com sucesso.");
    }//GEN-LAST:event_btnInserirFichaActionPerformed

    private void btnInserirReparoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirReparoActionPerformed
        // TODO add your handling code here:
        if (listaFichas.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Cadastre uma ficha antes de inserir reparos.");
            return;
        }

        if (listaCategorias.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Cadastre uma categoria antes de inserir reparos.");
            return;
        }

        if (listaProfissional.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Cadastre um mecânico antes de inserir reparos.");
            return;
        }

        int codigoFicha = lerInteiro("Digite o código da ficha:");

        if (codigoFicha == -1) {
            return;
        }

        FichaManutencao ficha = principal.buscarFichaCodigo(codigoFicha);

        if (ficha == null) {
            JOptionPane.showMessageDialog(null, "Ficha não encontrada.");
            return;
        }

        String nome = JOptionPane.showInputDialog("Digite o nome do reparo:");

        if (nome == null || nome.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nome do reparo obrigatório.");
            return;
        }

        double custo = lerDouble("Digite o custo do reparo:");

        if (custo == -1) {
            return;
        }

        String relato = JOptionPane.showInputDialog("Digite o relato do reparo:");

        if (relato == null) {
            relato = "";
        }

        int codigoCategoria = lerInteiro("Digite o código da categoria:");

        if (codigoCategoria == -1) {
            return;
        }

        Categoria categoria = principal.buscarCategoriaCodigo(codigoCategoria);

        if (categoria == null) {
            JOptionPane.showMessageDialog(null, "Categoria não encontrada.");
            return;
        }

        int codigoProfissional = lerInteiro("Digite o código do profissional:");

        if (codigoProfissional == -1) {
            return;
        }

        Profissional profissional = principal.buscarProfissionalCodigo(codigoProfissional);

        if (profissional == null) {
            JOptionPane.showMessageDialog(null, "Profissional não encontrado.");
            return;
        }

        ficha.inserirReparo(nome, custo, relato, categoria, profissional);

        JOptionPane.showMessageDialog(null, "Reparo inserido com sucesso.");
    }//GEN-LAST:event_btnInserirReparoActionPerformed

    private void btnDadosTesteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDadosTesteActionPerformed
        // TODO add your handling code here:
         if (listaVeiculos.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Cadastre veículos de teste primeiro na tela de veículos.");
            return;
        }

        if (listaCategorias.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Cadastre categorias de teste primeiro na tela de categorias.");
            return;
        }

        if (listaProfissional.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Cadastre mecânicos de teste primeiro na tela de mecânicos.");
            return;
        }

        Veiculo veiculo = listaVeiculos.get(0);

        if (principal.buscarFichaPorPlaca(veiculo.getPlaca()) != null) {
            JOptionPane.showMessageDialog(null, "O primeiro veículo da lista já possui ficha.");
            return;
        }

        int codigoFicha = 1;

        while (principal.buscarFichaCodigo(codigoFicha) != null) {
            codigoFicha++;
        }

        FichaManutencao ficha = new FichaManutencao(codigoFicha, veiculo);

        Categoria categoria = listaCategorias.get(0);
        Profissional profissional = listaProfissional.get(0);

        ficha.inserirReparo(
                "Troca de pastilhas",
                250.00,
                "Pastilhas desgastadas.",
                categoria,
                profissional
        );

        ficha.inserirReparo(
                "Alinhamento",
                120.00,
                "Veículo puxando para a direita.",
                categoria,
                profissional
        );

        listaFichas.add(ficha);

        JOptionPane.showMessageDialog(null, "Ficha de teste cadastrada com reparos.");
        
    }//GEN-LAST:event_btnDadosTesteActionPerformed

