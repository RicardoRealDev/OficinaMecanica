    private void btnEditarReparoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarReparoActionPerformed
        // TODO add your handling code here:
         int codigoFicha = lerInteiro("Digite o código da ficha:");

        if (codigoFicha == -1) {
            return;
        }

        FichaManutencao ficha = principal.buscarFichaCodigo(codigoFicha);

        if (ficha == null) {
            JOptionPane.showMessageDialog(null, "Ficha não encontrada.");
            return;
        }

        int codigoReparo = lerInteiro("Digite o código do reparo:");

        if (codigoReparo == -1) {
            return;
        }

        Reparo reparo = null;

        for (Reparo r : ficha.getListaReparos()) {
            if (r.getCodigo() == codigoReparo) {
                reparo = r;
                break;
            }
        }

        if (reparo == null) {
            JOptionPane.showMessageDialog(null, "Reparo não encontrado.");
            return;
        }

        String novoNome = JOptionPane.showInputDialog("Digite o novo nome:", reparo.getNome());

        if (novoNome != null && !novoNome.trim().isEmpty()) {
            reparo.setNome(novoNome);
        }

        double novoCusto = lerDouble("Digite o novo custo:");

        if (novoCusto != -1) {
            reparo.setCusto(novoCusto);
        }

        String novoRelato = JOptionPane.showInputDialog("Digite o novo relato:", reparo.getRelato());

        if (novoRelato != null) {
            reparo.setRelato(novoRelato);
        }

        int novoCodigoCategoria = lerInteiro("Digite o novo código da categoria:");

        if (novoCodigoCategoria != -1) {
            Categoria novaCategoria = principal.buscarCategoriaCodigo(novoCodigoCategoria);

            if (novaCategoria != null) {
                reparo.setCategoria(novaCategoria);
            } else {
                JOptionPane.showMessageDialog(null, "Categoria não encontrada. Categoria antiga mantida.");
            }
        }

        int novoCodigoProfissional = lerInteiro("Digite o novo código do profissional:");

        if (novoCodigoProfissional != -1) {
            Profissional novoProfissional = principal.buscarProfissionalCodigo(novoCodigoProfissional);

            if (novoProfissional != null) {
                reparo.setProfissional(novoProfissional);
            } else {
                JOptionPane.showMessageDialog(null, "Profissional não encontrado. Profissional antigo mantido.");
            }
        }

        JOptionPane.showMessageDialog(null, "Reparo editado com sucesso.");
    }//GEN-LAST:event_btnEditarReparoActionPerformed

